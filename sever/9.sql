SET NAMES UTF8;
DROP DATABASE IF EXISTS tedu;
CREATE DATABASE tedu CHARSET=UTF8;
USE tedu;
CREATE TABLE dept(
	did INT PRIMARY KEY, 
	dname VARCHAR(16) NOT NULL,
	location VARCHAR(16) NOT NULL DEFAULT '北京',
	empCount INT NOT NULL DEFAULT '0'
);
