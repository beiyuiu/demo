CREATE TABLE emp(
	eid INT PRIMARY KEY AUTO_INCREMENT,
	ename VARCHAR(16) NOT NULL,
	idCardNum CHAR(13) NOT NULL UNIQUE,
	salary DECIMAL(6,2) DEFAULT NULL,
	deptId INT,
	FOREIGN KEY(deptId) REFERENCES dept(did)
